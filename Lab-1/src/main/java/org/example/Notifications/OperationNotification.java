package org.example.Notifications;

/**
 * The enum Operation notification.
 */
public enum OperationNotification {
    /**
     * Success remaining balance operation notification.
     */
    SUCCESS_REMAINING_BALANCE,
    /**
     * Wrong account status operation notification.
     */
    WRONG_ACCOUNT_STATUS,
    /**
     * Not possible operation notification.
     */
    NOT_POSSIBLE,
    /**
     * Not enough money remaining balance operation notification.
     */
    NOT_ENOUGH_MONEY_REMAINING_BALANCE,
}
